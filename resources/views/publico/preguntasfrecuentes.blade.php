              <h5>1. ¿Como puedo hacer un préstamo?</h5>
              <p id="p1">Debe presentar el carné estudiantil vigente o documento de identidad, junto con el recibo de pago de matrícula financiera.</p>
              <hr>

              <h5>2. ¿Cuántos libros puedo tener en préstamo?</h5>
              <p id="p2">Si usted es estudiante, profesor, egresado o empleado activo de la Universidad, puede tener de 3 a 10 libros segun el tipo de usuario</p>
              <hr>

              <h5>3. ¿Puedo llevar en calidad de préstamo las revistas?</h5>
              <p id="p3">               
               Sí, pero  solo los  servicios en el sitio y que no requieran carné.
               </p>
              <hr>

              <h5>4.¿Puedo llevar en calidad de préstamo los Libros de Referencia?</h5>
              <p id="p4">Sí, estos materiales de referencia (Atlas, Diccionarios; Handbook, Thesauros, etc.) no tienen préstamo externo, solamente se facilitan para consulta  durante la jornada laboral de la Biblioteca.</p>
              <hr>

              <h5>5. ¿Dónde devuelvo los libros o materiales que tengo en préstamo?</h5>
              <p id="p5">
                Los libros se deben devolver en la misma Biblioteca donde se realizó el préstamo.
               </p>
              <hr>

              <h5>6. Se me quedó el carné, ¿puedo hacer uso de los servicios de la Biblioteca?</h5>
              <p id="p6">               
               Sí, pero  solo los  servicios en el sitio y que no requieran carné.
               </p>
              <hr>

              <h5>7. ¿Qué debo hacer en caso de pérdida del carné?</h5>
              <p id="p7">               
               Reportar la pérdida o extravío en la oficina de Registro y Control y solicitar un nuevo carné. Si requiere hacer uso de los servicios de la Biblioteca debe presentar recibo de trámite de copia de carné y documento de identidad.
               </p>
              <hr>
              
              <h5>8.¿Qué debo hacer en caso de que se me pierda  algún material bibliográfico?</h5>
              <p id="p8">               
                     Informar la pérdida del material en la Biblioteca central del Campus universitario  personalmente.  </p>
              <hr>
            
              <h5>9.¿Qué debo hacer si se me dañó un material bibliográfico?</h5>
              <p id="p9">               
                     Reportar el daño a la Biblioteca. Si el daño es superficial debe acercarse a la Biblioteca en la cual le prestaron el material para su posible reparación. Si el daño es grave y no es posible su reparación debe hacer la reposición del mismo material (título, autor y última edición), en caso de no encontrar el material en el mercado, la Biblioteca le dirá como reemplazarlo. Y cancelar el reproceso (bandas de seguridad, códigos internos).
               </p>
              <hr>
             
              <h5>10. ¿Si tengo una multa en el sistema de Bibliotecas me afecta en la prestación de algún  servicio?</h5>
              <p id="p10">               
                     Afecta en el préstamo de material bibliográfico externo (para la casa) y bloqueo en el Sistema de Academusoft, lo que no le permite descargar el recibo de pago para la Matrícula Financiera del siguiente período.               </p>
              <hr>

              <h5>11. ¿Cómo sé si tengo una multa pendiente?</h5>
              <p id="p11"> 
                     OPCIÓN I. Ingresar con su usuario y contraseña a su cuenta en el catalogo en linea (OPAC) y dirigise a la opción consultar multas <br>
                     OPCIÓN II. Ingresar al sito web de biblioteca e ir a la opción <a href="estatus">estatus de biblioteca</a> 
              </p>             
              <hr>
         
              <h5>12. ¿Tengo que devolver los libros para pagar la multa?</h5>
                     <p id="p12"> 
                     Sí, es necesario hacer la devolución de los materiales con préstamos vencidos para que el sistema genere la multa correspondiente.
                     </p>             
              <hr>

              <h5>13. ¿Cómo puedo generar el recibo para pagar una multa y dónde la puedo pagar?</h5>
              <p id="p13"> 
                            Para el pago de multas se debe dirigir a la página web <a href="http://www.unipacifico.edu.co:8095/web3.0/inicio.jsp"> www.unipacifico.edu.co</a> , e iniciar sesión en su cuenta de Academusoft. Al interior de su cuenta dar clic   en la opción pedido IG y posteriormente se gestiona el recibo para descargarlo. Una vez descargado el recibido puede acercarse a las cajas registradoras del Éxito, ubicadas en el centro comercial Viva Buenaventura o en su defecto en el Banco de Occidente.
                            Si presenta dificultad para descargar el recibo de multas, puede acercarse a la Biblioteca más cercana de la Universidad del Pacifico y le facilitarán la gestión. 
                            
              </p>             
             <hr>

             <h5>14.Después de pagar mi multa, ¿Cuánto tiempo debo esperar para acceder al servicio de préstamo?</h5>
                     <p id="p14"> 
                    Una vez haya realizado el pago de la multa en el banco, debe acercarse a la Biblioteca con el recibo para que la persona encargada pueda hacer el respectivo registro  en el sistema, e inmediatamente puede hacer uso de los servicios de  préstamos sin inconveniente alguno.                    
               </p>             
              <hr>

              <h5>15.¿Puedo acceder a la consulta de bases de datos si tengo multas?</h5>
              <p id="p15"> 
              Sí, aun cuando tenga multa en el Sistema, puede consultar nuestros recursos electrónicos.   
            </p>             
               <hr>


               <h5>16.¿Qué tengo que hacer si soy estudiante de otra universidad y quiero consultar la Biblioteca de la Universidad del Pacífico?</h5>
               <p id="p16"> 
                     La Biblioteca atiende a los usuarios remitidos por instituciones con las cuales existe un convenio interbibliotecario vigente, para lo cual los usuarios deben  presentar  su carné vigente.   
               </p>             
               <hr>

               
               <h5>17. ¿Tengo algún privilegio como estudiante tesista?</h5>
               <p id="p17"> 
                            Sí, el privilegio es el aumento en la cantidad de préstamos (10 ítems de libros de Colección Abierta por un período de 14 días, renovables). Para recibir este privilegio se debe informar a la  Biblioteca por parte de la Facultad, Departamento o tutor asignado, su nuevo estado como "estudiante en tesis" vía comunicación oficial  o correo electrónico.               </p>             
               <hr>

           
               <h5>18. Si soy egresado(a), ¿Puedo hacer uso de los servicios que presta la Biblioteca de la Universidad del Pacífico?</h5>
               <p id="p18"> 
                     Identificándose en la  Biblioteca con su carné de egresado, usted puede hacer uso en sala de las colecciones y servicios existentes, pero no podrá solicitar  préstamo externo.
               </p>             
